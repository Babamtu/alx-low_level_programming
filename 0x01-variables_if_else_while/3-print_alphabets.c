#include <stdio.h>
/**
 *main - Entry point
 *
 *a program to print capital and small letters using putchar
 *Return: 0 (Success)
 */

int main(void)
{
	char c;

	char d;

	c = 'a';
	d = 'A';
	while
		(c <= 'z') {
		putchar(c);
		c++;
	}
	while
		(d <= 'z') {
		putchar(c);
		d++;
	}
	return (0);
}

